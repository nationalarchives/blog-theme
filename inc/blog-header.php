  <h3>Writer of the month: Dominic Sandbrook</h3>
  <p>People often think that writing about very recent history is much easier than writing about, say, the Anglo-Saxons, because there are simply so many more sources. There&#39;s some truth in that, but there are drawbacks, too. As the furore over Lady Thatcher&#39;s funeral suggests, most of us find it hard to contemplate events in living memory with the scholarly detachment that we might bring to the distant past. Even very recent events, such as the Winter of Discontent, the Falklands War and the 1984 miners&#39; strike, are now laden down with all sort of ideological baggage and journalistic mythology.</p>
