			<h2><span><span><a href="http://blog.nationalarchives.gov.uk/blog/new-files-from-1983/" title="Permalink to Let&#8217;s chat" rel="bookmark">New files from 1983 – Thatcher meets Solzhenitsyn</a></span></span></h2>
		</div>

			<div class="entry-meta">
                
                 by <a href="http://blog.nationalarchives.gov.uk/blog/author/sdemissie/" title="Posts by Simon Demissie">Simon Demissie</a> in <a href="http://blog.nationalarchives.gov.uk/blog/category/records-research/" title="View all posts in Records and research" rel="category tag">Records and research</a> on Thursday 1 August, 2013
       
			
<span class="call-to-action-link comment">
                <a href="http://blog.nationalarchives.gov.uk/blog/lets-chat/#respond" rel="nofollow" title="Comment on Let&#8217;s chat">Comment</a>                </span></div>
   
<p>As we move towards a <a href="http://www.nationalarchives.gov.uk/about/20-year-rule.htm">20 year disclosure of public records</a>, today we mark the first mid-year release of government files. Previously secret files from 1983 have been released, offering us new insight into the Prime Minister’s in-tray that year. Themes of interest from the year include the workings of the Conservative government either side of a landslide election victory, the testing of the hitherto solid relationship between Ronald Reagan and Margaret Thatcher following the US-led invasion of Grenada, and the discussions behind the placement of US missiles at Greenham Common.</p>
<div id="attachment_11165" class="wp-caption aligncenter" style="width: 540px"><img class="size-Full width wp-image-11165 colorbox-11163" src="http://blog.nationalarchives.gov.uk/wp-content/uploads/2013/07/Evstafiev-solzhenitsyn-530x384.jpg" alt="Aleksandr Solzhenitsyn on his return to Russia in 1994" width="530" height="384" /><p class="wp-caption-text">Aleksandr Solzhenitsyn on his return to Russia in 1994 (Copyright: Evstafiev GFDL - http://commons.wikimedia.org/wiki/File:A_solzhenitsin.JPG) (CC-BY-SA-2.5 http://creativecommons.org/licenses/by-sa/2.5)</p></div>
<p>The sheer weight of paperwork remains astounding, as hundreds of Prime Minister’s Office files, each page of which would have passed over Thatcher’s desk, have been made available in the reading rooms at The National Archives, Kew. There is evidence again of the Prime Minister’s tendency to heavily annotate documents, with some particularly virulent comments regarding work by Geoffrey Howe and Nigel Lawson, which perhaps indicates a degree of post-election frustration and impatience at the relatively sluggish pace of change. 
